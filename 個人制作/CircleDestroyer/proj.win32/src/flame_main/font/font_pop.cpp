//-------------------------------------------------------------------------------------------------
//	文字列の表示
//-------------------------------------------------------------------------------------------------
void font_pop_drawfunc( TASK *ap )
{
	TASK spr;
	FLOAT font_scale;
	SINT32* string_addr;
	SINT32 string_box;
	SINT32 pop_font_counter = 0;


	string_addr = ap->work_p[X];
	string_box = *string_addr;

	TASK_init(&spr);

	spr.pos[X] = ap->pos[X];
	spr.pos[Y] = ap->pos[Y];
	spr.pos[Z] = ap->pos[Z];
	spr.base_color[0][0] = ap->base_color[0][0];
	spr.base_color[0][1] = ap->base_color[0][1];
	spr.base_color[0][2] = ap->base_color[0][2];
	spr.base_color[0][3] = ap->base_color[0][3];
	spr.scale[X] *= ap->FONT_SIZE;
	spr.scale[Y] = spr.scale[X];
	spr.uv_rect = font_rect;

	while ( string_box != 0xff )
	{
		spr.anime_no = string_box;

		if (string_box > 0xbb)
		{
			spr.tex_no = TEXTURE_FONT_AS;
			font_scale = 0.25f;
		}
		else
		{
			spr.tex_no = TEXTURE_FONT_JS;
			font_scale = 1.0f;
		}

		switch (string_box)
		{
			case 0xfe:
				//空白
				spr.pos[X] += (FONT_TEXTURE_SIZE * ap->FONT_INTERVAL_X) * ap->FONT_SIZE;
				break;

			case 0xfd:
				//改行
				spr.pos[X] = ap->pos[X];
				spr.pos[Y] += ( FONT_TEXTURE_SIZE * ap->FONT_INTERVAL_Y )* ap->FONT_SIZE;
				//string_addr += STRING_MAX_DATA - pop_font_counter - 1;
				pop_font_counter = -1;
				break;

			case 0xfc:
				//文字の色の変更
				pop_font_counter++;
				string_addr++;
				string_box = *string_addr;
				spr.base_color[ 0 ][ 0 ] = string_box;
				/*if( ap->workF[X] != 0 )//２回め
				{
					ap->workF[X] = 0;
					spr.base_color[0][0] = 255;
					spr.base_color[0][1] = 255;
					spr.base_color[0][2] = 255;
					spr.base_color[0][3] = 255;
				}
				else
				{
					ap->workF[X] = 1;
					spr.base_color[0][0] = ap->workE[X];
					spr.base_color[0][1] = ap->workE[Y];
					spr.base_color[0][2] = ap->workE[Z];
					spr.base_color[0][3] = ap->workE[P];
				}*/
				break;

			case 0xfb:
				pop_font_counter++;
				string_addr++;
				string_box = *string_addr;
				spr.base_color[ 0 ][ 1 ] = string_box;
				break;

			case 0xfa:
				pop_font_counter++;
				string_addr++;
				string_box = *string_addr;
				spr.base_color[ 0 ][ 2 ] = string_box;
				break;

			case 0xf9:
				pop_font_counter++;
				string_addr++;
				string_box = *string_addr;
				spr.base_color[ 0 ][ 3 ] = string_box;
				break;

			default:
				if (string_box < 0xd4)
				{
					spr.scale[X] *= font_scale;
					spr.scale[Y] *= font_scale;
					DRAW_default_func( &spr );
					spr.scale[X] /= font_scale;
					spr.scale[Y] /= font_scale;
					if (string_box >= 0xbc)
					{
						spr.pos[X] += ( ALPHABET_TEXTURE_SIZE * ap->FONT_INTERVAL_X ) * ap->FONT_SIZE * font_scale;
					}
					else
					{
						spr.pos[X] += ( FONT_TEXTURE_SIZE * ap->FONT_INTERVAL_X ) * ap->FONT_SIZE * font_scale;	
					}
				}
				break;
		}
		pop_font_counter++;
		string_addr++;
		string_box = *string_addr;
	}
}

//-------------------------------------------------------------------------------------------------
//	文字列の表示
//	font_sizeは1.0でdefaultの文字の大きさになる
//	font_INTERVALは1.0fでdefaultと同じ間隔になる
//-------------------------------------------------------------------------------------------------
TASK* font_pop_start( FUNC prg_point, FLOAT point_x, FLOAT point_y, FLOAT font_size, FLOAT font_interval_x, FLOAT font_interval_y, SINT32* string)
{
	TASK* ap;
	ap = TASK_start( prg_point, "テキストの表示" );
	ap->drawfunc = font_pop_drawfunc;
	ap->pos[X] = point_x;
	ap->pos[Y] = point_y;
	ap->FONT_SIZE = font_size;
	ap->FONT_INTERVAL_X = font_interval_x;
	ap->FONT_INTERVAL_Y = font_interval_y;
	ap->pri = PRI_FONT;
	ap->work_p[X] = string;
	ap->workF[X] = 0;		//色の変更用フラグ
	ap->workE[X] = 0xff;
	ap->workE[Y] = 0xff;
	ap->workE[Z] = 0xff;
	ap->workE[P] = 0xff;
	return ap;
}

//-------------------------------------------------------------------------------------------------
//	数字をだすときの専門
//	ap->work1[X]を変えると数字が変わる
//-------------------------------------------------------------------------------------------------
void font_pop_default_number_drawfunc(TASK* ap)
{
	TASK spr;
	SINT8 str[256];
	SINT32 number;
	SINT32 num_digit = 10;
	SINT32 roop = 0;

	number = ap->work1[X];

	TASK_init( &spr );
	spr.pos[X] = ap->pos[X];
	spr.pos[Y] = ap->pos[Y];
	spr.base_color[0][0] = ap->base_color[0][0];
	spr.base_color[0][1] = ap->base_color[0][1];
	spr.base_color[0][2] = ap->base_color[0][2];
	spr.base_color[0][3] = ap->base_color[0][3];
	spr.scale[X] = ap->FONT_SIZE;
	spr.scale[Y] = ap->FONT_SIZE;
	spr.tex_no = TEXTURE_FONT_JS;
	spr.uv_rect = font_rect;

	do
	{
		spr.anime_no = number % num_digit;
		spr.anime_no += 0xaf;
		//SOZ_to_g_spr( NULL, &spr );
		DRAW_default_func( &spr );
		number /= num_digit;
		roop++;
		spr.pos[X] -= 48.0f * ap->FONT_INTERVAL_X * ap->FONT_SIZE;
	} while (number > 0 && roop < 10 );


#ifdef DEBUG
	/*sprintf(str, "x : %f y: %f num %d", ap->pos[X], ap->pos[Y], ap->work1[X]);
	SOZ_debug_string(1.0f, 8.0f, str);*/

#endif // DEBUG

}

TASK* font_pop_number(FUNC prg_point, FLOAT point_x, FLOAT point_y, FLOAT font_size, SINT32 number)
{
	TASK* ap;

	ap = TASK_start(prg_point, "数字の表示");
	ap->drawfunc = font_pop_default_number_drawfunc;
	ap->pos[X] = point_x;
	ap->pos[Y] = point_y;
	
	ap->FONT_SIZE = font_size;
	ap->FONT_INTERVAL_X = 1.0f;
	ap->FONT_INTERVAL_Y = 1.0f;
	ap->CURSOR_POINT = 0;
	ap->pri = PRI_FONT + 5;
	ap->work1[X] = number;
	ap->work1[Y] = 0;

	return ap;
}

